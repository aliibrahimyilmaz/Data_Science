# Aufgabe: Lambda Funktion schreiben, die Fläche eines Rechtecks oder Kreises beschreibt

area_rechteck = lambda a, b: a * b
print("die Fläche eines Rechtecks: {} cm2 ".format(area_rechteck(3, 5)))

area_kreis = lambda r: 3.14 * r**2
print("die Fläche eines Kreises: {} cm2".format(area_kreis(3)))




